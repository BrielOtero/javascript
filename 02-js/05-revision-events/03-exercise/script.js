function select(i) {
  switch (i) {
    case 1:
      document.getElementById(i).style.display = "block";

      break;
    case 2:
      document.getElementById(i).style.display = "block";

      break;
    case 3:
      document.getElementById(i).style.display = "block";

      break;
    case 4:
      break;
  }
}

function hideSelect(i) {
  document.getElementById(i).style.display = "none";
}
